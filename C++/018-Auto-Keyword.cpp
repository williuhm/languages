/*
    If the "auto" keyword does what I think, which is automatically
    type cast a variable, that would be neat.

    And it does exactly that, wow.
*/

#include <iostream>
using namespace std;

int main() {
    // Creating auto variables
    auto myNum = 5; // int
    auto myFloatNum = 5.99f; // float
    auto myDoubleNum = 9.98; // double
    auto myLetter = 'D'; // char
    auto myBoolean = true; // bool
    auto myString = string("Hello"); // std::string
    return 0;
}